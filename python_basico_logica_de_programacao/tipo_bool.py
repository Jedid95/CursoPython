# Tipo de dado bool (boolean)
# Ao questionar algo em um programa, só existem duas respostas possiveis:
#     sim(true) ou não (false)
# Existem varios operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que questiona se um valor é igual ao outro

print(10 == 10)
print(10 == 11)