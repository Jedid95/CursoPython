"""
+ - Adição
- - Subtração
* - Multiplicação
/ - Divisão
// - Divisão inteira
** - Exponenciação
% - Módulo - Resto da divisão
() - Alterar as precedencias nas formulas
"""

print('Multiplicação *', 10 * 10)
print('Adição +', 10 + 10)
print('Subtração -', 10 - 5)
print('Divisão /', 10 / 2)
print('Divisão nº inteiro //', 10 // 3)
print('Exponenciação **', 2 ** 10)
print('Módulo - Resto da divisão', 4 % 5)
